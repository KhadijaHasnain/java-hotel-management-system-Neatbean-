    private void savechangesbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savechangesbuttonActionPerformed
        String updatedFirstName = newfirstnamefield.getText();
        String updatedLastName = newlastnamefield.getText();
        String updatedIcPassport = newicpassportfield.getText();
        String updatedUsername = newusernamefield.getText();
        String updatedPassword = new String(newpasswordfield.getPassword());
        String confirmPassword = new String(newconfirmpasswordfield.getPassword());

        // Check if the new password and confirm password fields match
        if (!updatedPassword.isEmpty() && !updatedPassword.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match!");
            return;
        }

        String filePath = "record.txt";
        StringBuilder updatedContent = new StringBuilder();

        try (BufferedReader reader = new BufferedReader(new FileReader(filePath))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(";");

                if (parts.length >= 5 && parts[3].equals(loggedInUsername)) {
                    // Only update the fields that are not empty
                    if (!updatedFirstName.isEmpty()) {
                        parts[0] = updatedFirstName;
                    }
                    if (!updatedLastName.isEmpty()) {
                        parts[1] = updatedLastName;
                    }
                    if (!updatedIcPassport.isEmpty()) {
                        parts[2] = updatedIcPassport;
                    }
                    if (!updatedUsername.isEmpty()) {
                        parts[3] = updatedUsername;
                    }
                    if (!updatedPassword.isEmpty()) {
                        parts[4] = updatedPassword;
                    }

                    // Reconstruct the updated line
                    line = String.join(";", parts);

                    // Update the loggedInUsername if the username was changed
                    loggedInUsername = parts[3];
                }
                updatedContent.append(line).append(System.lineSeparator());
            }
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error reading file: " + e.getMessage());
            return;
        }

        // Write the updated content back to the file, replacing the old data
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(filePath))) {
            writer.write(updatedContent.toString());
            JOptionPane.showMessageDialog(this, "Profile updated successfully!");

            new HomePage(OrderDetails,TotalAmount,loggedInUsername,OrderID).setVisible(true);
            dispose();

        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error saving file: " + e.getMessage());
        }       // TODO add your handling code here:
    }//GEN-LAST:event_savechangesbuttonActionPerformed

