    private void loadUserProfile() {
        String filePath = "record.txt"; // Update this path to your actual file location
        try (BufferedReader reader = new BufferedReader(new FileReader(filePath))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(";");

                // Check if the username matches the logged-in username
                if (parts.length >= 5 && parts[3].equals(loggedInUsername)) {
                    // Populate the text fields with the user's data
                    firstnamefield.setText(parts[0]);
                    lastnamefield.setText(parts[1]);
                    icpassportfield.setText(parts[2]);
                    usernamefield.setText(parts[3]);
                    passwordfield.setText(parts[4]);
                    confirmpasswordfield.setText(parts[4]); // Confirm password is the same as password
                    break; // Stop reading after finding the correct user
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
            // Optionally show an error message if the file can't be read
            javax.swing.JOptionPane.showMessageDialog(this, "Error loading profile: " + e.getMessage());
        }
    }

