    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            try {
                String name = (String) jTable1.getValueAt(selectedRow, 0);
                menuManager.deleteMenuItem(name);
                refreshTable();
            } catch (RemoteException e) {
                e.printStackTrace();
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedRow = jTable1.getSelectedRow();

        // Check if no row is selected
        if (selectedRow == -1) {
            // Show an error message dialog
            JOptionPane.showMessageDialog(this, "Please select an item in the table first.", "No Item Selected", JOptionPane.WARNING_MESSAGE);
            return; // Exit the method to prevent further execution
        }

        // If a row is selected, populate fields with the selected row data
        populateFieldsWithSelectedRow();
        showButton();

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            // Retrieve the edited values
            String name = jTextField1.getText();
            String description = jTextField2.getText();
            double price = Double.parseDouble(jTextField3.getText());
            String category = (String) jComboBox1.getSelectedItem(); // Get selected category from combo box

            // Update the item in the data source
            try {
                menuManager.updateMenuItem(new MenuItem(name, description, price, category));
            } catch (RemoteException e) {
                e.printStackTrace();
            }

            // Refresh the table
            refreshTable();

            // Hide Save and Discard buttons
            jButton4.setVisible(false);
            jButton5.setVisible(false);

//            // Optionally disable the text fields and combo box
//            jTextField1.setEnabled(false);
//            jTextField2.setEnabled(false);
//            jTextField3.setEnabled(false);
//            jComboBox1.setEnabled(false); // Disable combo box
        }
    }//GEN-LAST:event_jButton4ActionPerformed

