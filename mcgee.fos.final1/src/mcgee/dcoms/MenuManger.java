    private void connectToServer() {
        try {
            LocateRegistry.getRegistry(1099); // Connect to RMI registry
            menuManager = (MenuManagerInt) Naming.lookup("rmi://localhost/MenuManager");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Failed to connect to the server: " + e.getMessage(), "Connection Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void refreshTable() {
        try {
            List<MenuItem> items = menuManager.getAllMenuItems();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Clear existing rows

            for (MenuItem item : items) {
                model.addRow(new Object[]{item.getName(), item.getDescription(), item.getPrice(), item.getCategory()});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void populateFieldsWithSelectedRow() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            // Assuming you have columns: Name, Description, Price, Category
            String name = (String) jTable1.getValueAt(selectedRow, 0);
            String description = (String) jTable1.getValueAt(selectedRow, 1);
            double price = (Double) jTable1.getValueAt(selectedRow, 2);
            String category = (String) jTable1.getValueAt(selectedRow, 3);

            // Set values to your text fields
            jTextField1.setText(name);
            jTextField2.setText(description);
            jTextField3.setText(String.valueOf(price));
            jComboBox1.setSelectedItem(category);
        }
    }

